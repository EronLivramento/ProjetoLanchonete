    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (tblUser.getSelectedRow() == -1) {
            Message.addMessageError(this, "Por favor selecione o usuario que deseja alterar!", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        FrmUpdateUser dialog = new FrmUpdateUser(new javax.swing.JFrame(), true, userService, this, listUser.get(tblUser.getSelectedRow()));
        dialog.setVisible(true);

    }//GEN-LAST:event_btnUpdateActionPerformed

