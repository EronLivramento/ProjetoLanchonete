    public void loadInitalData() {
        try {
            listUser = userService.findAllUser();
        } catch (ServiceException ex) {
            Message.addMessage(this, ex.getMessage());
        }
        tblUser.setModel(new MyTableModel(User.class, listUser, tblUser));
    }
